package fieldformat;

public class CompanyDetails {
	private String companyName;

	private String amountOfSales;

	private String grossProfit;

	private String operatingIncome;

	private String managementProfit;

	private String netIncomeBeforeTax;

	private String netIncome;

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	public String getAmountOfSales() {
		return amountOfSales;
	}

	public void setAmountOfSales(String amountOfSales) {
		this.amountOfSales = amountOfSales;
	}

	public String getGrossProfit() {
		return grossProfit;
	}

	public void setGrossProfit(String grossProfit) {
		this.grossProfit = grossProfit;
	}

	public String getOperatingIncome() {
		return operatingIncome;
	}

	public void setOperatingIncome(String operatingIncome) {
		this.operatingIncome = operatingIncome;
	}

	public String getManagementProfit() {
		return managementProfit;
	}

	public void setManagementProfit(String managementProfit) {
		this.managementProfit = managementProfit;
	}

	public String getNetIncomeBeforeTax() {
		return netIncomeBeforeTax;
	}

	public void setNetIncomeBeforeTax(String netIncomeBeforeTax) {
		this.netIncomeBeforeTax = netIncomeBeforeTax;
	}

	public String getNetIncome() {
		return netIncome;
	}

	public void setNetIncome(String netIncome) {
		this.netIncome = netIncome;
	}


}
